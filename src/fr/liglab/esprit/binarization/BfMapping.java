package fr.liglab.esprit.binarization;

public class BfMapping {
	public static void main(String[] args) {
		final float[] weights = new float[] { 0.31902095675468f, -0.10216334462166f, 0.19273310899734f,
				0.32204401493073f, 0.27967050671577f, -0.422507584095f, -0.13158525526524f, -0.13707286119461f,
				-0.15632206201553f, 0.35391440987587f, 0.31582149863243f, -0.20533810555935f, -0.011241439729929f,
				0.2515749335289f, -0.23724322021008f, 0.32144799828529f, -0.132448092103f, 0.034225426614285f,
				0.36499634385109f, -0.012439073994756f, 0.2970729470253f, -0.29502502083778f, 0.38541573286057f,
				-0.31018736958504f, -0.37365034222603f, -0.16247120499611f, 0.17832064628601f };
		float sum = 0.31911900639534f;
		int nbSum = 0;
		for (int i = 0; i < weights.length; i++) {
			if (i % 3 != 0 && i % 9 > 2) {
				sum -= weights[i];
				nbSum++;
			}
		}
		System.out.println(nbSum + " " + sum);
	}
}
